import django_filters
from django_filters.filters import BaseInFilter, NumberFilter
from .models import PhototypeExecuteProject, PhototypeExecuteProjectDetails, PTEFiles


class NumberInFilter(BaseInFilter, NumberFilter):
    pass


class PhototypeExecuteProjectFilter(django_filters.FilterSet):
    name = django_filters.CharFilter(lookup_expr='icontains')
    created_time = django_filters.DateTimeFromToRangeFilter()

    class Meta:
        model = PhototypeExecuteProject
        fields = "__all__"


class PhototypeExecuteProjectDetailsFilter(django_filters.FilterSet):
    name = django_filters.CharFilter(lookup_expr='icontains')
    created_time = django_filters.DateTimeFromToRangeFilter()

    class Meta:
        model = PhototypeExecuteProjectDetails
        fields = "__all__"


class PTEFilesFilter(django_filters.FilterSet):
    name = django_filters.CharFilter(lookup_expr='icontains')
    created_time = django_filters.DateTimeFromToRangeFilter()

    class Meta:
        model = PTEFiles
        fields = "__all__"

